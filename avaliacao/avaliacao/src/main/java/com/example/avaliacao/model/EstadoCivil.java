package com.example.avaliacao.model;

// Enumeração que define os possíveis valores para o estado civil de um funcionário
public enum EstadoCivil {
    // Representa o estado civil de uma pessoa que não é casada
    SOLTEIRO,

    // Representa o estado civil de uma pessoa que é casada
    CASADO,

    // Representa o estado civil de uma pessoa que se divorciou
    DIVORCIADO,

    // Representa o estado civil de uma pessoa que é viúva
    VIUVO;
}

